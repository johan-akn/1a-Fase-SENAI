/* Faça um programa no qual o usuário digite dois números e mostre na tela a multiplicação desses números.*/

//Declarar variáveis
let numeroUm, numeroDois, resultadoMultiplicacao


//Entrada de dados
numeroUm = prompt('Digite o primeiro número')
numeroDois = prompt('Digite o segundo número')


//Cálculos
resultadoMultiplicacao = numeroUm * numeroDois


//Saída de dados
alert('\nResultado da multiplicação: ' + resultadoMultiplicacao)