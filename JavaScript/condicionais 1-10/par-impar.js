/*Programar para que o usuário digite um número.
 Caso seja par, mostre "O número é par!", senão, mostre "O número é ímpar!"*/


 //Declarar variáveis
 let numeroDigitado, relDois = 2, relZero


 //Entrada de dados
 numeroDigitado = prompt('Digite um número: ')

 
 //Saída de dados
 if(numeroDigitado % relDois == relZero){

    alert('O número digitado é par.')

 }
 else{

    alert('O número digitado é ímpar.')

 }